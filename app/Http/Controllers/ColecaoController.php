     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        //valida as entradas
        $request->validate([
            'nome' => 'required|max:255',
        ]);

        //salva o usuario no banco de dados
        $colecao = new Colecao([
            'nome' => $request->get('nome')
        ]);

        $colecao->save();

        return redirect()->route('colecao.index')->with('success','Coleção cadastrado com sucesso');
    }

